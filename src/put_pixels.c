#include "fdf.h"

int			put_pixels(t_fdf *fdf)
{
	int	x;
	int	y;

	y = 0;
	while (y < fdf->height)
	{
		x = 0;
		while (x < fdf->width)
		{
			put_line(fdf, x, y);
			++x;
		}
		++y;
	}
	return (0);
}
