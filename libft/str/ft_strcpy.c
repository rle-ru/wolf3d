char	*ft_strcpy(char *dst, const char *src)
{
	char	*ret;

	ret = dst;
	if (!dst || !src)
		return (0);
	while (*src)
	{
		*dst = *src;
		++dst;
		++src;
	}
	*dst = '\0';
	return (ret);
}
