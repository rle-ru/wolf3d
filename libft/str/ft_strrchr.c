#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ret;

	ret = 0;
	while (*s)
	{
		if (*s == (char)c)
			ret = (char*)s;
		s++;
	}
	if (c == '\0')
		ret = (char*)&s[ft_strlen(s)];
	return (ret);
}
