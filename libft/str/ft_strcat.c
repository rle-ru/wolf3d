#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	ret;

	if (!s1 || !s2)
		return (0);
	ret = s1;
	s1 += ft_strlen(s1);
	while (*s2)
	{
		*s1 = *s2;
		++s1;
		++s2;
	}
	*s1 = '\0';
	return (ret);
}
