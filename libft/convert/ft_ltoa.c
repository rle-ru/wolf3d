#include "libft.h"

char		*ft_ltoa(long n)
{
	char	buff[21];
	int		i;
	long	nb;

	buff[20] = 0;
	buff[19] = '0';
	i = 19;
	nb = n;
	while (nb)
	{
		buff[i] = (ft_abs(nb % 10)) + '0';
		nb /= 10;
		--i;
	}
	if (n < 0)
	{
		buff[i] = '-';
		--i;
	}
	return (ft_strdup(buff + i + 1));
}
